public class updating {
    public static void main(String[] args) {
        int x = 8;
        System.out.println(x);
        x = 10;
        System.out.println(x);
        x = x+6;
        System.out.println(x);
        x = x - 14;
        System.out.println(x); 
        x = x * 3;
        System.out.println(x);
        x = x / 4;
        System.out.println(x);
    }
}
