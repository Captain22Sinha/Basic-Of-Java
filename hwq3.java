public class hwq3 {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("Original Value: " +x);
        x = x+1;
        System.out.println("After Increment: " +x);
        x = x-1;
        System.out.println("After Decement" +x);
        x = x%10;
        System.out.println("Remainder: " +x);
    }
}
